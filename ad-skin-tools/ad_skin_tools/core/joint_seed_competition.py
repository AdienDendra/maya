from dataclasses import dataclass
from typing import Dict, List, Optional, Sequence, Set, Tuple
import time

import maya.api.OpenMaya as om
import maya.cmds as cmds

from ad_skin_tools.core.compat import ensure_numpy
from ad_skin_tools.core.joint_surface_solver import JointSeedResult
from ad_skin_tools.core.mesh import (
    get_dag_path,
    get_vertex_count,
    get_vertex_normals,
    get_vertex_positions,
    get_weighted_vertex_neighbors,
    get_world_positions,
)

np = ensure_numpy()


@dataclass(frozen=True)
class JointCompetitionOptions:
    """
    AD Skin Tool v2.6 Phase 2.1.

    Every raw surface candidate is evaluated against every joint.

    No joint hierarchy, parent-child relation, or bone segment is used.
    """

    # Joint should normally be behind the outward vertex normal.
    #
    # -1.0 = directly inward
    #  0.0 = tangent
    # +1.0 = outward
    inward_dot_threshold: float = 0.10

    # Secondary preference after world-space joint distance.
    normal_alignment_weight: float = 0.35

    # Hard line-of-sight validation:
    #
    # joint -> candidate vertex
    #
    # The first mesh intersection must belong to a face connected
    # to the target vertex.
    require_visibility: bool = True

    ray_tolerance: float = 1e-6

    # Numerical distance tolerance is based on the local mesh edge length.
    visibility_tolerance_fraction: float = 0.15
    minimum_visibility_tolerance: float = 1e-4

    minimum_resolved_seed_count: int = 4

    # False is useful during research because all joint diagnostics and
    # seed sets remain available even when one joint receives few seeds.
    fail_on_invalid_joint: bool = False


@dataclass(frozen=True)
class JointCompetitionDiagnostic:
    joint: str

    raw_candidate_count: int

    strict_visible_raw_count: int
    relaxed_visible_raw_count: int

    won_before_component_filter: int
    resolved_seed_count: int
    removed_disconnected_count: int

    anchor_vertex_id: int
    anchor_mode: str
    anchor_score: float

    valid: bool
    messages: Tuple[str, ...]


@dataclass(frozen=True)
class CompetitiveSeedResult:
    mesh_shape: str
    influences: Tuple[str, ...]

    resolved_seed_vertex_ids: Dict[
        str,
        Tuple[int, ...],
    ]

    anchor_vertex_ids: Dict[
        str,
        int,
    ]

    diagnostics: Tuple[
        JointCompetitionDiagnostic,
        ...,
    ]

    raw_unique_candidate_count: int
    resolved_unique_seed_count: int

    visibility_test_count: int
    visibility_fallback_vertex_count: int

    elapsed_seconds: float


def resolve_joint_seed_competition(
    seed_result: JointSeedResult,
    options: Optional[JointCompetitionOptions] = None,
) -> CompetitiveSeedResult:
    """
    Resolve raw radial candidates through global joint competition.

    Important difference from the previous resolver:

    A candidate originally generated by ring_001 is still compared with
    pinky_001, hand_BND, and every other joint.

    Therefore a pinky-area candidate cannot remain with ring_001 merely
    because pinky_001 did not generate that exact candidate vertex.
    """
    started = time.perf_counter()

    options = options or JointCompetitionOptions()
    _validate_options(options)

    mesh_shape = seed_result.mesh_shape

    if not mesh_shape or not cmds.objExists(mesh_shape):
        raise RuntimeError(
            "The mesh from the Phase-1 result no longer exists."
        )

    influences = tuple(seed_result.influences)

    if len(influences) < 2:
        raise RuntimeError(
            "At least two joints are required."
        )

    vertex_count = get_vertex_count(mesh_shape)

    if vertex_count <= 0:
        raise RuntimeError(
            "The mesh contains no vertices."
        )

    vertex_ids = np.arange(
        vertex_count,
        dtype=np.int32,
    )

    positions = get_vertex_positions(
        mesh_shape,
        vertex_ids,
    )

    normals = get_vertex_normals(
        mesh_shape,
        vertex_ids,
    )

    adjacency = get_weighted_vertex_neighbors(
        mesh_shape
    )

    joint_positions = get_world_positions(
        list(influences)
    )

    raw_by_joint: List[Set[int]] = []

    raw_union: Set[int] = set()

    for joint in influences:
        raw_candidates = {
            int(vertex_id)
            for vertex_id
            in seed_result.seed_vertex_ids.get(
                joint,
                (),
            )
        }

        if not raw_candidates:
            raise RuntimeError(
                "Joint has no Phase-1 candidates:\n{}".format(
                    joint
                )
            )

        _validate_vertex_ids(
            vertex_ids=raw_candidates,
            vertex_count=vertex_count,
            joint=joint,
        )

        raw_by_joint.append(
            raw_candidates
        )

        raw_union.update(
            raw_candidates
        )

    union_ids = np.asarray(
        sorted(raw_union),
        dtype=np.int32,
    )

    union_row_by_vertex = {
        int(vertex_id): row
        for row, vertex_id
        in enumerate(union_ids)
    }

    union_positions = positions[
        union_ids
    ]

    union_normals = normals[
        union_ids
    ]

    scores, inward_dots = _build_score_matrices(
        vertex_positions=union_positions,
        vertex_normals=union_normals,
        joint_positions=joint_positions,
        normal_alignment_weight=float(
            options.normal_alignment_weight
        ),
    )

    local_edge_scales = _local_edge_scales(
        adjacency
    )

    mesh_dag_path = get_dag_path(
        mesh_shape
    )

    mesh_fn = om.MFnMesh(
        mesh_dag_path
    )

    accel_params = mesh_fn.autoUniformGridParams()

    connected_faces = _connected_faces_by_vertex(
        mesh_dag_path,
        vertex_count,
    )

    visibility = np.ones(
        (
            len(union_ids),
            len(influences),
        ),
        dtype=bool,
    )

    visibility_test_count = 0

    try:
        if options.require_visibility:
            visibility.fill(False)

            for union_row, vertex_id in enumerate(
                union_ids
            ):
                vertex_id = int(vertex_id)

                tolerance = max(
                    float(
                        options.minimum_visibility_tolerance
                    ),
                    float(
                        options.visibility_tolerance_fraction
                    )
                    * float(
                        local_edge_scales[
                            vertex_id
                        ]
                    ),
                )

                target_position = positions[
                    vertex_id
                ]

                target_faces = connected_faces[
                    vertex_id
                ]

                for joint_index in range(
                    len(influences)
                ):
                    visibility_test_count += 1

                    visibility[
                        union_row,
                        joint_index,
                    ] = _joint_can_see_vertex(
                        mesh_fn=mesh_fn,
                        accel_params=accel_params,
                        joint_position=joint_positions[
                            joint_index
                        ],
                        target_position=target_position,
                        target_faces=target_faces,
                        distance_tolerance=tolerance,
                        ray_tolerance=float(
                            options.ray_tolerance
                        ),
                    )

    finally:
        try:
            mesh_fn.freeCachedIntersectionAccelerator()

        except Exception:
            pass

    strict = (
        visibility
        & (
            inward_dots
            <= float(
                options.inward_dot_threshold
            )
        )
    )

    locked_owner_by_vertex, anchor_modes = _assign_unique_anchors(
        influences=influences,
        raw_by_joint=raw_by_joint,
        union_row_by_vertex=union_row_by_vertex,
        scores=scores,
        visibility=visibility,
        strict=strict,
    )

    owner_by_vertex: Dict[int, int] = {}

    fallback_vertex_count = 0

    for union_row, vertex_id in enumerate(
        union_ids
    ):
        vertex_id = int(vertex_id)

        if vertex_id in locked_owner_by_vertex:
            owner_by_vertex[
                vertex_id
            ] = int(
                locked_owner_by_vertex[
                    vertex_id
                ]
            )

            continue

        strict_joint_ids = np.where(
            strict[
                union_row
            ]
        )[0]

        if strict_joint_ids.size:
            owner_index = _best_joint(
                scores=scores[
                    union_row
                ],
                candidate_joint_ids=strict_joint_ids,
            )

        else:
            visible_joint_ids = np.where(
                visibility[
                    union_row
                ]
            )[0]

            if visible_joint_ids.size:
                owner_index = _best_joint(
                    scores=scores[
                        union_row
                    ],
                    candidate_joint_ids=visible_joint_ids,
                )

            else:
                # This should be rare. Keep it visible in diagnostics.
                fallback_vertex_count += 1

                owner_index = int(
                    np.argmin(
                        scores[
                            union_row
                        ]
                    )
                )

        owner_by_vertex[
            vertex_id
        ] = owner_index

    won_before_filter: List[Set[int]] = [
        set()
        for _ in influences
    ]

    for vertex_id, owner_index in owner_by_vertex.items():
        won_before_filter[
            owner_index
        ].add(
            vertex_id
        )

    anchor_by_joint = {
        owner_index: vertex_id
        for vertex_id, owner_index
        in locked_owner_by_vertex.items()
    }

    resolved_by_joint: List[Set[int]] = [
        set()
        for _ in influences
    ]

    removed_disconnected_counts = [
        0
        for _ in influences
    ]

    for joint_index in range(
        len(influences)
    ):
        owned_vertices = won_before_filter[
            joint_index
        ]

        anchor_vertex_id = anchor_by_joint[
            joint_index
        ]

        anchor_component = _component_containing_anchor(
            candidate_ids=owned_vertices,
            anchor_vertex_id=anchor_vertex_id,
            adjacency=adjacency,
        )

        resolved_by_joint[
            joint_index
        ] = anchor_component

        removed_disconnected_counts[
            joint_index
        ] = (
            len(owned_vertices)
            - len(anchor_component)
        )

    diagnostics = []
    invalid_diagnostics = []

    for joint_index, joint in enumerate(
        influences
    ):
        raw_candidates = raw_by_joint[
            joint_index
        ]

        strict_visible_raw_count = 0
        relaxed_visible_raw_count = 0

        for vertex_id in raw_candidates:
            union_row = union_row_by_vertex[
                vertex_id
            ]

            if strict[
                union_row,
                joint_index,
            ]:
                strict_visible_raw_count += 1

            elif visibility[
                union_row,
                joint_index,
            ]:
                relaxed_visible_raw_count += 1

        anchor_vertex_id = anchor_by_joint[
            joint_index
        ]

        anchor_row = union_row_by_vertex[
            anchor_vertex_id
        ]

        anchor_score = float(
            scores[
                anchor_row,
                joint_index,
            ]
        )

        resolved_count = len(
            resolved_by_joint[
                joint_index
            ]
        )

        messages = []

        if resolved_count < int(
            options.minimum_resolved_seed_count
        ):
            messages.append(
                "only {} connected seeds remain".format(
                    resolved_count
                )
            )

        valid = not messages

        diagnostic = JointCompetitionDiagnostic(
            joint=joint,

            raw_candidate_count=len(
                raw_candidates
            ),

            strict_visible_raw_count=(
                strict_visible_raw_count
            ),

            relaxed_visible_raw_count=(
                relaxed_visible_raw_count
            ),

            won_before_component_filter=len(
                won_before_filter[
                    joint_index
                ]
            ),

            resolved_seed_count=resolved_count,

            removed_disconnected_count=(
                removed_disconnected_counts[
                    joint_index
                ]
            ),

            anchor_vertex_id=anchor_vertex_id,

            anchor_mode=anchor_modes[
                joint_index
            ],

            anchor_score=anchor_score,

            valid=valid,

            messages=tuple(
                messages
            ),
        )

        diagnostics.append(
            diagnostic
        )

        if not valid:
            invalid_diagnostics.append(
                diagnostic
            )

    if (
        invalid_diagnostics
        and options.fail_on_invalid_joint
    ):
        lines = [
            "Global joint seed competition failed:",
            "",
        ]

        for diagnostic in invalid_diagnostics:
            lines.append(
                "{}: {}".format(
                    diagnostic.joint,
                    "; ".join(
                        diagnostic.messages
                    ),
                )
            )

        raise RuntimeError(
            "\n".join(
                lines
            )
        )

    resolved_seed_vertex_ids = {
        joint: tuple(
            sorted(
                resolved_by_joint[
                    joint_index
                ]
            )
        )
        for joint_index, joint
        in enumerate(influences)
    }

    anchor_vertex_ids = {
        joint: int(
            anchor_by_joint[
                joint_index
            ]
        )
        for joint_index, joint
        in enumerate(influences)
    }

    resolved_union = set()

    for resolved_ids in resolved_by_joint:
        resolved_union.update(
            resolved_ids
        )

    return CompetitiveSeedResult(
        mesh_shape=mesh_shape,
        influences=influences,

        resolved_seed_vertex_ids=(
            resolved_seed_vertex_ids
        ),

        anchor_vertex_ids=(
            anchor_vertex_ids
        ),

        diagnostics=tuple(
            diagnostics
        ),

        raw_unique_candidate_count=len(
            raw_union
        ),

        resolved_unique_seed_count=len(
            resolved_union
        ),

        visibility_test_count=(
            visibility_test_count
        ),

        visibility_fallback_vertex_count=(
            fallback_vertex_count
        ),

        elapsed_seconds=(
            time.perf_counter()
            - started
        ),
    )


def select_competitive_joint_seeds(
    result: CompetitiveSeedResult,
    joint: str,
) -> None:
    joint_path = _resolve_result_joint(
        result,
        joint,
    )

    components = _vertex_components(
        mesh_shape=result.mesh_shape,
        vertex_ids=result.resolved_seed_vertex_ids[
            joint_path
        ],
    )

    cmds.select(
        clear=True
    )

    if components:
        cmds.select(
            components,
            replace=True,
        )


def select_competitive_anchor(
    result: CompetitiveSeedResult,
    joint: str,
) -> None:
    joint_path = _resolve_result_joint(
        result,
        joint,
    )

    vertex_id = result.anchor_vertex_ids[
        joint_path
    ]

    components = _vertex_components(
        mesh_shape=result.mesh_shape,
        vertex_ids=(
            vertex_id,
        ),
    )

    cmds.select(
        components,
        replace=True,
    )


def create_competitive_seed_sets(
    result: CompetitiveSeedResult,
    prefix: str = "AD_v26_competitiveSeed",
) -> List[str]:
    created_sets = []

    for joint in result.influences:
        short_name = joint.split(
            "|"
        )[-1]

        set_name = "{}_{}".format(
            prefix,
            short_name,
        )

        if cmds.objExists(
            set_name
        ):
            cmds.delete(
                set_name
            )

        components = _vertex_components(
            mesh_shape=result.mesh_shape,
            vertex_ids=result.resolved_seed_vertex_ids[
                joint
            ],
        )

        created_sets.append(
            cmds.sets(
                components,
                name=set_name,
            )
        )

    return created_sets


