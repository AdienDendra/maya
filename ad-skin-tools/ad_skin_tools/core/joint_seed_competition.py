def _build_score_matrices(
    vertex_positions: np.ndarray,
    vertex_normals: np.ndarray,
    joint_positions: np.ndarray,
    normal_alignment_weight: float,
) -> Tuple[np.ndarray, np.ndarray]:
    to_joint = (
        joint_positions[
            np.newaxis,
            :,
            :
        ]
        - vertex_positions[
            :,
            np.newaxis,
            :
        ]
    )

    distances = np.linalg.norm(
        to_joint,
        axis=2,
    )

    safe_distances = np.maximum(
        distances,
        1e-12,
    )

    directions = (
        to_joint
        / safe_distances[
            :,
            :,
            np.newaxis,
        ]
    )

    inward_dots = np.einsum(
        "vji,vi->vj",
        directions,
        vertex_normals,
    )

    alignment_error = (
        0.5
        * (
            1.0
            + np.clip(
                inward_dots,
                -1.0,
                1.0,
            )
        )
    )

    # Absolute world-space distance is deliberately used.
    #
    # Do not normalize by each joint's local radius because that makes
    # scores from different joints incomparable.
    scores = (
        distances
        * (
            1.0
            + float(
                normal_alignment_weight
            )
            * alignment_error
            * alignment_error
        )
    )

    return (
        scores,
        inward_dots,
    )


def _assign_unique_anchors(
    influences: Sequence[str],
    raw_by_joint: List[Set[int]],
    union_row_by_vertex: Dict[int, int],
    scores: np.ndarray,
    visibility: np.ndarray,
    strict: np.ndarray,
) -> Tuple[
    Dict[int, int],
    Dict[int, str],
]:
    pairs = []

    for joint_index in range(
        len(influences)
    ):
        for vertex_id in raw_by_joint[
            joint_index
        ]:
            row = union_row_by_vertex[
                vertex_id
            ]

            if strict[
                row,
                joint_index,
            ]:
                mode_rank = 0
                mode_name = "strict-visible"

            elif visibility[
                row,
                joint_index,
            ]:
                mode_rank = 1
                mode_name = "relaxed-visible"

            else:
                mode_rank = 2
                mode_name = "visibility-fallback"

            pairs.append(
                (
                    mode_rank,
                    float(
                        scores[
                            row,
                            joint_index,
                        ]
                    ),
                    int(
                        joint_index
                    ),
                    int(
                        vertex_id
                    ),
                    mode_name,
                )
            )

    pairs.sort()

    assigned_joints = set()
    assigned_vertices = set()

    locked_owner_by_vertex = {}
    anchor_modes = {}

    for (
        _mode_rank,
        _score,
        joint_index,
        vertex_id,
        mode_name,
    ) in pairs:
        if joint_index in assigned_joints:
            continue

        if vertex_id in assigned_vertices:
            continue

        assigned_joints.add(
            joint_index
        )

        assigned_vertices.add(
            vertex_id
        )

        locked_owner_by_vertex[
            vertex_id
        ] = joint_index

        anchor_modes[
            joint_index
        ] = mode_name

        if len(
            assigned_joints
        ) == len(
            influences
        ):
            break

    missing = [
        influences[
            joint_index
        ]
        for joint_index in range(
            len(influences)
        )
        if joint_index not in assigned_joints
    ]

    if missing:
        raise RuntimeError(
            "Could not reserve unique anchors for:\n{}".format(
                "\n".join(
                    missing
                )
            )
        )

    return (
        locked_owner_by_vertex,
        anchor_modes,
    )


def _best_joint(
    scores: np.ndarray,
    candidate_joint_ids: np.ndarray,
) -> int:
    candidate_scores = scores[
        candidate_joint_ids
    ]

    local_index = int(
        np.argmin(
            candidate_scores
        )
    )

    return int(
        candidate_joint_ids[
            local_index
        ]
    )


def _joint_can_see_vertex(
    mesh_fn,
    accel_params,
    joint_position: np.ndarray,
    target_position: np.ndarray,
    target_faces: Set[int],
    distance_tolerance: float,
    ray_tolerance: float,
) -> bool:
    vector = (
        target_position
        - joint_position
    )

    target_distance = float(
        np.linalg.norm(
            vector
        )
    )

    if target_distance <= 1e-12:
        return True

    direction = (
        vector
        / target_distance
    )

    try:
        hit = mesh_fn.closestIntersection(
            om.MFloatPoint(
                float(
                    joint_position[0]
                ),
                float(
                    joint_position[1]
                ),
                float(
                    joint_position[2]
                ),
            ),
            om.MFloatVector(
                float(
                    direction[0]
                ),
                float(
                    direction[1]
                ),
                float(
                    direction[2]
                ),
            ),
            om.MSpace.kWorld,
            float(
                target_distance
                + distance_tolerance
            ),
            False,
            accelParams=accel_params,
            tolerance=float(
                ray_tolerance
            ),
        )

    except RuntimeError:
        return False

    if not hit:
        return False

    hit_point = hit[0]
    hit_distance = float(
        hit[1]
    )
    hit_face_id = int(
        hit[2]
    )

    hit_position = np.asarray(
        [
            hit_point.x,
            hit_point.y,
            hit_point.z,
        ],
        dtype=np.float64,
    )

    hit_to_target_distance = float(
        np.linalg.norm(
            hit_position
            - target_position
        )
    )

    reaches_target_distance = (
        hit_distance
        >= target_distance
        - distance_tolerance
    )

    reaches_target_position = (
        hit_to_target_distance
        <= distance_tolerance
    )

    hits_target_face = (
        hit_face_id
        in target_faces
    )

    return (
        hits_target_face
        and (
            reaches_target_distance
            or reaches_target_position
        )
    )


def _connected_faces_by_vertex(
    mesh_dag_path,
    vertex_count: int,
) -> List[Set[int]]:
    result = [
        set()
        for _ in range(
            vertex_count
        )
    ]

    iterator = om.MItMeshVertex(
        mesh_dag_path
    )

    while not iterator.isDone():
        vertex_id = int(
            iterator.index()
        )

        result[
            vertex_id
        ] = {
            int(face_id)
            for face_id
            in iterator.getConnectedFaces()
        }

        iterator.next()

    return result


def _local_edge_scales(
    adjacency,
) -> np.ndarray:
    result = np.ones(
        len(adjacency),
        dtype=np.float64,
    )

    all_edge_lengths = []

    for vertex_id, neighbors in enumerate(
        adjacency
    ):
        lengths = [
            float(edge_length)
            for _, edge_length
            in neighbors
            if float(edge_length) > 1e-12
        ]

        if lengths:
            result[
                vertex_id
            ] = float(
                np.median(
                    np.asarray(
                        lengths,
                        dtype=np.float64,
                    )
                )
            )

            all_edge_lengths.extend(
                lengths
            )

    if all_edge_lengths:
        global_scale = float(
            np.median(
                np.asarray(
                    all_edge_lengths,
                    dtype=np.float64,
                )
            )
        )

        invalid = result <= 1e-12
        result[
            invalid
        ] = global_scale

    return result


def _component_containing_anchor(
    candidate_ids: Set[int],
    anchor_vertex_id: int,
    adjacency,
) -> Set[int]:
    if anchor_vertex_id not in candidate_ids:
        raise RuntimeError(
            "Protected anchor is not present in its owner's candidate set."
        )

    visited = {
        int(
            anchor_vertex_id
        )
    }

    stack = [
        int(
            anchor_vertex_id
        )
    ]

    while stack:
        vertex_id = stack.pop()

        for neighbor_id, _ in adjacency[
            vertex_id
        ]:
            neighbor_id = int(
                neighbor_id
            )

            if neighbor_id not in candidate_ids:
                continue

            if neighbor_id in visited:
                continue

            visited.add(
                neighbor_id
            )

            stack.append(
                neighbor_id
            )

    return visited


def _validate_vertex_ids(
    vertex_ids: Set[int],
    vertex_count: int,
    joint: str,
) -> None:
    invalid = [
        vertex_id
        for vertex_id in vertex_ids
        if (
            vertex_id < 0
            or vertex_id >= vertex_count
        )
    ]

    if invalid:
        raise RuntimeError(
            "Invalid candidate vertex IDs for joint:\n{}\n{}".format(
                joint,
                invalid[:20],
            )
        )


def _resolve_result_joint(
    result: CompetitiveSeedResult,
    joint: str,
) -> str:
    matches = cmds.ls(
        joint,
        long=True,
        type="joint",
    ) or []

    if not matches:
        raise RuntimeError(
            "Joint does not exist: {}".format(
                joint
            )
        )

    joint_path = matches[0]

    if joint_path not in result.resolved_seed_vertex_ids:
        raise RuntimeError(
            "Joint was not included in this result:\n{}".format(
                joint_path
            )
        )

    return joint_path


def _vertex_components(
    mesh_shape: str,
    vertex_ids,
) -> List[str]:
    parents = cmds.listRelatives(
        mesh_shape,
        parent=True,
        fullPath=True,
    ) or []

    mesh_name = (
        parents[0]
        if parents
        else mesh_shape
    )

    return [
        "{}.vtx[{}]".format(
            mesh_name,
            int(
                vertex_id
            ),
        )
        for vertex_id in vertex_ids
    ]


def _validate_options(
    options: JointCompetitionOptions,
) -> None:
    if not -1.0 <= float(
        options.inward_dot_threshold
    ) <= 1.0:
        raise ValueError(
            "inward_dot_threshold must be between -1.0 and 1.0."
        )

    if float(
        options.normal_alignment_weight
    ) < 0.0:
        raise ValueError(
            "normal_alignment_weight cannot be negative."
        )

    if float(
        options.ray_tolerance
    ) <= 0.0:
        raise ValueError(
            "ray_tolerance must be positive."
        )

    if float(
        options.visibility_tolerance_fraction
    ) <= 0.0:
        raise ValueError(
            "visibility_tolerance_fraction must be positive."
        )

    if float(
        options.minimum_visibility_tolerance
    ) <= 0.0:
        raise ValueError(
            "minimum_visibility_tolerance must be positive."
        )

    if int(
        options.minimum_resolved_seed_count
    ) < 1:
        raise ValueError(
            "minimum_resolved_seed_count must be at least 1."
        )